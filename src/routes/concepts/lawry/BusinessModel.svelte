	<h3>Defendability & strategy</h3>
	<p>
		<span class="font-bold">Phase 1 - </span>The easiest way to start is by building a user-friendly interface on top of <i>ChatGPT</i>. In that stage the underlying technology is basically 'outsourced' and can not be defended. However, this is the time to start building a group of recurring users and generate a bit of revenue to proof product-market fit.
	</p>
    <p>
		<span class="font-bold">Phase 2 - </span>As both a bit of revenue, as well as legal questions starts coming in, we can start to hire lawyers to train the responses and build out the dataset. This 'augmentation' on the AI technology must gradually develop into a competitive advantage. This should be augmented further with custom indexations of databases. <i>The quality and reliability of the responses will then become a key feature of the service. </i>
	</p>
    <p>
		<span class="font-bold">Phase 3 - </span>As time goes on we can assume competing AI technologies will get better, thus lowering the added value of the dataset as a competitive advantage. By this time, the userbase and familiarity of Lawry with clients' businesses becomes the greatest asset. Simultaneously the selection of legal firms and lawyers on the platform will become a strategic advantage.
	</p>
    
